#include "part.h"
#include "main.h"

void sbit(unsigned int bit, unsigned int adr)
{
    writel((readl(adr)) | bit, adr);
}

void cbit(unsigned int bit, unsigned int adr)
{
    writel((readl(adr)) & ~bit, adr);
}
